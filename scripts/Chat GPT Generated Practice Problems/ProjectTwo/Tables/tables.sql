CREATE TABLE DEPARTMENT(
    DEPARTMENT_ID NUMBER PRIMARY KEY,
    DEPARTMENT_NAME VARCHAR2(100)
);

CREATE TABLE EMPLOYEE (
    ID NUMBER primary key,
    FIRST_NAME varchar2(100),
    LAST_NAME varchar2(100),
    JOB_TITLE varchar2(50),
    DEPARTMENT_ID NUMBER,
    SALARY NUMBER(7, 2),
    E_MAIL varchar2(1000),
    HIRE_DATE date,
    constraint fk_dep_id foreign key(DEPARTMENT_ID) REFERENCES DEPARTMENT(DEPARTMENT_ID)
);

CREATE TABLE EMPLOYEE_AUDIT (
    AUDIT_ID NUMBER generated by default as identity primary key,
    EMPLOYEE_ID NUMBER,
    FIRST_NAME VARCHAR2(100),
    LAST_NAME VARCHAR2(100),
    JOB_TITLE VARCHAR2(50),
    DEPARTMENT_ID NUMBER,
    SALARY NUMBER(7, 2),
    EMAIL VARCHAR2(1000),
    HIRE_DATE DATE,
    UPDATE_BY VARCHAR2(250),
    UPDATE_ON TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    constraint fk_emp_id foreign key(EMPLOYEE_ID) REFERENCES EMPLOYEE(ID),
    constraint fk_depart_id foreign key(DEPARTMENT_ID) REFERENCES DEPARTMENT(DEPARTMENT_ID)
);

commit;
